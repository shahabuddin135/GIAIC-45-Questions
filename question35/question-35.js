/*Animals: Think of at least three different animals that have
a common characteristic. Store the names of these animals in a
list, and then use a for loop to print out the name of each animal.
*/
var animals = ['cat', 'dog', 'horse'];
for (var pet = 0; pet < animals.length; pet++) {
    console.log("".concat(animals[pet], " would make a great pet!"));
}
console.log("'cat, dog and horse is a domestic animal\nthey are very good friends to humans.'");
